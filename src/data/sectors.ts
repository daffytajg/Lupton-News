export function getSectorById(id: Sector): SectorInfo | undefined {
  return SECTORS.find(s => s.id === id);
}

export function getCategoryById(id: NewsCategory): NewsCategoryInfo | undefined {
  return NEWS_CATEGORIES.find(c => c.id === id);
}

export function getCategoryPriorityColor(priority: string): string {
  switch (priority) {
    case 'critical': return 'text-red-600 bg-red-50 border-red-200';
    case 'high': return 'text-orange-600 bg-orange-50 border-orange-200';
    case 'medium': return 'text-yellow-600 bg-yellow-50 border-yellow-200';
    case 'low': return 'text-blue-600 bg-blue-50 border-blue-200';
    default: return 'text-gray-600 bg-gray-50 border-gray-200';
  }
}
