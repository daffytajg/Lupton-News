// Helper to get random source
const getSource = (sector: Sector): { source: string; sourceUrl: string } => {
  const sectorSources: Record<Sector, string[]> = {
    'datacenter': ['Reuters', 'Bloomberg', 'TechCrunch', 'Industry Week'],
    'heavy-trucks': ['Automotive News', 'Fleet Owner', 'Reuters', 'Industry Week'],
    'military-aerospace': ['Defense News', 'Aviation Week', 'SpaceNews', 'Reuters'],
    'robotics-automation': ['Robotics Business Review', 'Industry Week', 'Manufacturing.net', 'Reuters'],
    'medical-scientific': ['Medical Device Network', 'Modern Healthcare', 'Bloomberg', 'Reuters'],
  };
  const sources = sectorSources[sector] || ['Reuters', 'Bloomberg', 'Industry Week'];
  const source = sources[Math.floor(Math.random() * sources.length)];
  return { source, sourceUrl: NEWS_SOURCES[source as keyof typeof NEWS_SOURCES] || 'https://www.reuters.com' };
};

// Generate date strings for past articles
const getDateString = (daysAgo: number): string => {
  const date = new Date();
  date.setDate(date.getDate() - daysAgo);
  return date.toISOString();
};

