// Function to get articles for a specific company
export function getArticlesForCompany(companyId: string): NewsArticle[] {
  return ALL_COMPANY_ARTICLES.filter(article => 
    article.companies.includes(companyId)
  ).sort((a, b) => 
    new Date(b.publishedAt).getTime() - new Date(a.publishedAt).getTime()
  );
}

// Function to get article count for a company
export function getArticleCountForCompany(companyId: string): number {
  return ALL_COMPANY_ARTICLES.filter(article => 
    article.companies.includes(companyId)
  ).length;
}

// Function to get all articles sorted by date
export function getAllArticlesSorted(): NewsArticle[] {
  return [...ALL_COMPANY_ARTICLES].sort((a, b) => 
    new Date(b.publishedAt).getTime() - new Date(a.publishedAt).getTime()
  );
}

