// Get articles for a specific company
export function getArticlesForCompany(companyId: string): NewsArticle[] {
  return HISTORICAL_ARTICLES.filter(article => 
    article.companies.includes(companyId)
  ).sort((a, b) => new Date(b.publishedAt).getTime() - new Date(a.publishedAt).getTime());
}

// Get article count for a company
export function getHistoricalArticleCount(companyId: string): number {
  return HISTORICAL_ARTICLES.filter(article => 
    article.companies.includes(companyId)
  ).length;
}

// Get all articles sorted by date
export function getAllArticlesSortedByDate(): NewsArticle[] {
  return [...HISTORICAL_ARTICLES].sort((a, b) => 
    new Date(b.publishedAt).getTime() - new Date(a.publishedAt).getTime()
  );
}

// Get articles by category
export function getArticlesByCategory(category: NewsCategory): NewsArticle[] {
  return HISTORICAL_ARTICLES.filter(article => article.categories.includes(category))
    .sort((a, b) => new Date(b.publishedAt).getTime() - new Date(a.publishedAt).getTime());
}

// Get articles by sector
export function getArticlesBySector(sector: Sector): NewsArticle[] {
  return HISTORICAL_ARTICLES.filter(article => article.sectors.includes(sector))
    .sort((a, b) => new Date(b.publishedAt).getTime() - new Date(a.publishedAt).getTime());
}

// Export article counts by company for quick lookup
export const HISTORICAL_ARTICLE_COUNTS: Record<string, number> = {};
HISTORICAL_ARTICLES.forEach(article => {
  article.companies.forEach(companyId => {
    HISTORICAL_ARTICLE_COUNTS[companyId] = (HISTORICAL_ARTICLE_COUNTS[companyId] || 0) + 1;
  });
});
